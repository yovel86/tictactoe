package machine_coding.tictactoe.models;

public class RowColumn {

    private int row;
    private int col;

    public RowColumn(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

}
